const map = input.split('\n');

const tree = '#';
const space = '.';

const steps = [
  [1, 1],
  [3, 1],
  [5, 1],
  [7, 1],
  [1, 2],
];

let product = 1;

for (const step of steps) {
  let pos = [0, 0];

  const encounter = { tree: 0, space: 0 };

  while (pos[1] !== map.length - 1) {
    // make step
    pos = pos.map((p, i) => p + step[i]);

    if (pos[0] >= map[0].length) {
      pos[0] %= map[0].length;
    }

    //check what is there
    const curr = map[pos[1]][pos[0]];

    if (curr === tree) encounter.tree++;
    else if (curr === space) encounter.space++;
  }

  product *= encounter.tree;

  console.log({ product, encounter });
}
